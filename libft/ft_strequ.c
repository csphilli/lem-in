#include "./header/libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	if (s1[0] != '\0' && !(*(s1) ^ *(s2)))
	{
		s1++;
		s2++;
	}
	return (!(*(s1) ^ *(s2)));
}
